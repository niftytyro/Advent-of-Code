int solve(char *input) {
  int floor = 0;

  for (int i = 0; i < strlen(input); i++) {
    if (input[i] == '(') {
      floor++;
    }
    if (input[i] == ')') {
      floor--;
    }

    if (floor == -1) {
      printf("Index is %d\n", i + 1);
    }
  }

  return floor;
}

